<h1>About Us</h1>
<p>Local Photography is a start-up company located in sunny Indianola, Iowa.  Our guiding vision is creating a safe place in which everyone can find and develop their inner photographer.  Here at Local Photography, we believe that every person has an inner photographer that simply must be unleashed before they can become a beast with a camera.  Think you cannot start photography classes because you know nothing about photography?  Think again! We have had dozens of students walk through our doors without the faintest idea of what a camera is!  Do not worry, we will help you every step of the way. </p>

